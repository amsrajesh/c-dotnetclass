using System;

public class Program
{
    public static void Main()
    {
        for (int i = 10; i > 0; i--)
        {
            Console.WriteLine("Value of i: {0}", i);
            Console.ReadLine();
        }
        for (int j = 12; j > 0; j++)
        {
            Console.WriteLine("value of j:{0}", j);
            Console.ReadLine();

        }
    }
}